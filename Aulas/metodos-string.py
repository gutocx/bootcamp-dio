# Eliminando espaços em branco
branco = "   Python  "
print(branco)
print(branco.strip())
print(branco.lstrip())
print(branco.rstrip())
print()

# Junção e centralização
juncao = "Python"
print(juncao)
print(juncao.center(10, "#"))
print(".".join(juncao))